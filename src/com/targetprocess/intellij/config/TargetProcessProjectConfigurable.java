package com.targetprocess.intellij.config;

import com.intellij.openapi.options.Configurable;
import com.intellij.openapi.options.ConfigurationException;
import org.jetbrains.annotations.Nls;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;

import javax.swing.*;


public class TargetProcessProjectConfigurable implements Configurable
{
    private TargetProcessProjectConfigurableForm configurableForm;

    @Nls
    @Override
    public String getDisplayName() {
        return "TargetProcess";
    }

    @Nullable
    @Override
    public String getHelpTopic() {
        return "settings.targetprocess";
    }

    @Nullable
    @Override
    public JComponent createComponent() {
        if (configurableForm == null)
            configurableForm = new TargetProcessProjectConfigurableForm();
        reset();
        return configurableForm.getComponent();
    }

    @Override
    public boolean isModified() {
        return false;
    }

    @Override
    public void apply() throws ConfigurationException {

    }

    @Override
    public void reset() {

    }

    @Override
    public void disposeUIResources() {

    }
}
